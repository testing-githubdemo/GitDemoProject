package conditional;

public class if_1 {
	public static void main(String[] args) {
		int s=20;
		if(s>=20)
		{
			System.out.println("print"+s);
		}
		
		//System.out.println("out of condition");
		for(int y=2;y<=20;y=y+2)
		{
			System.out.println("even number "+y);
		}
		System.out.println("+++++++++++++++++");
		for(int y=1;y<=20;y=y+2)
		{
			System.out.println("odd number "+y);
		}
	}

}
