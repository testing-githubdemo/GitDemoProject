package loops;

public class elseif_loops4 {

	public static void main(String[] args) {
int marks=35;
if(marks>=70)
{
	System.out.println("Get Distinction");
}
else if(marks>=55)
{
	System.out.println("Get First Class");
}
else if(marks>=45)
{
	System.out.println("Get pass class");
}
else
{
	System.out.println("Fail");
}
	}

}
